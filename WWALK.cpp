void solve()
{
    // Write your solution code here
    int n;
    cin >> n;
    vector<int> a(n), b(n);
    for (auto &x : a)
        cin >> x;
    for (auto &x : b)
        cin >> x;

    int ans = 0, suma = 0, sumb = 0;
    for (int i = 0; i < n; i++)
    {
        if (suma == sumb and a[i] == b[i])
        {
            ans += a[i];
        }
        suma += a[i], sumb += b[i];
    }

    cout << ans << endl;
}

