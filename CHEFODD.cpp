void solve()
{

    int n, k;
    cin >> n >> k;

    if (n < 2 * k)
    {
        cout << "NO\n";
        return;
    }

    if ((n % 2 == 0 and k % 2 == 0) or (n & 1 and k & 1))
    {
        cout << "YES\n";
    }
    else
    {
        cout << "NO\n";
    }
}

